const GLYPHS: Record<string, Pixel[]> = {
  ashenford: ASHENFORD,
  mirecross: MIRECROSS,
  riversend: RIVERSEND,
  crownpost: CROWNPOST,
  fenward: FENWARD,
}

function glyphPixels(townId: string): Pixel[] {
  return GLYPHS[townId] ?? GLYPHS.ashenford
}

export interface LocationPixelIconProps {
  townId: TownId
  /** CSS pixel size (square). */
  size?: number
  className?: string
  title?: string
}

export function LocationPixelIcon({ townId, size = 32, className, title }: LocationPixelIconProps) {
  const pixels = glyphPixels(townId)
  return (
    <svg
      width={size}
      height={size}
      viewBox="-1 -1 18 18"
      className={className}
      shapeRendering="crispEdges"
      role={title ? 'img' : 'presentation'}
      aria-hidden={title ? undefined : true}
      aria-label={title}
    >
      {title ? <title>{title}</title> : null}
      {pixels.map(([x, y, fill], i) => (
        <rect key={`${x}-${y}-${i}`} x={x} y={y} width={1} height={1} fill={fill} />
      ))}
    </svg>
  )
}

export function MapLocationGlyph({
  townId,
  x,
  y,
  size = 7,
  opacity = 1,
}: {
  townId: TownId
  x: number
  y: number
  size?: number
  opacity?: number
}) {
  const pixels = glyphPixels(townId)
  const half = size / 2
  return (
    <g transform={`translate(${x - half}, ${y - half})`} opacity={opacity}>
      <svg
        width={size}
        height={size}
        viewBox="-1 -1 18 18"
        shapeRendering="crispEdges"
        overflow="visible"
      >
        {pixels.map(([px, py, fill], i) => (
          <rect key={`${px}-${py}-${i}`} x={px} y={py} width={1} height={1} fill={fill} />
        ))}
      </svg>
    </g>
  )
}
